          $scope.vitals = data.vitals;
          $scope.whichvital=$state.params.aId;
          $scope.data = { showDelete: false, showReorder: false };

          $scope.onItemDelete = function(item) {
            $scope.vitals.splice($scope.vitals.indexOf(item), 1);
          }

          $scope.doRefresh =function() {
          $http.get('http://50.172.239.7:34344/vitals.json').success(function(data) {
              $scope.vitals = data;
              $scope.$broadcast('scroll.refreshComplete');
            });
          }

          $scope.toggleStar = function(item) {
            item.star = !item.star;
          }

          $scope.moveItem = function(item, fromIndex, toIndex) {
            $scope.vitals.splice(fromIndex, 1);
            $scope.vitals.splice(toIndex, 0, item);
          };

